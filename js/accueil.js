// change la couleur de la bordure et du texte quand l'utilisateur passe sur la section
// section FILM
function hoverEffect() {
    document.getElementById("film").style.border="solid 3px red";
    document.getElementById("texteFilm").style.color="orange";

}

function removeHover() {
    document.getElementById("film").style.border = "";
    document.getElementById("texteFilm").style.color="";
}


